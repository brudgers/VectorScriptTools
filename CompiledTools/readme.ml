Here are 37 VectorScript commands compiled into tools and complete with icons. They are unencrypted so despite their binary format, the source code can be read with a decent text editor - I use Notpad++. 

Because they are commands boxed as tools, after clicking on the icon, a click in the drawing area is required.

The source code and icons for many, if not all of these tools is currently (2013-5-30) located elsewhere in the brudgers github repository.
